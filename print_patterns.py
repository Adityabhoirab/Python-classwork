# Right-Angle Triangle of Asterisks
for i in range(1, 6):
    for j in range(1, i + 1):
        print("*", end=" ")
    print()

# Right-Angle Triangle of Numbers
for i in range(1, 6):
    for j in range(1, i + 1):
        print(j, end=" ")
    print()

# Inverted Right-Angle Triangle of Asterisks
for i in range(5, 0, -1):
    for j in range(1, i + 1):
        print("*", end=" ")
    print()

# Inverted Right-Angle Triangle of Numbers
for i in range(5, 0, -1):
    for j in range(1, i + 1):
        print(j, end=" ")
    print()

