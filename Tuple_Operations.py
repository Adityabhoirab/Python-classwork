# Example tuple with repeated values
thistuple = ("apple", "banana", "cherry", "apple", "cherry")

# Print the tuple
print("The tuple is:", thistuple)

# Find the length of the tuple
print("Length of the tuple:", len(thistuple))

# Access elements by index (first and fourth elements)
print("First element:", thistuple[0])
print("Fourth element:", thistuple[3])

# Access a slice of the tuple (from index 2 to index 4)
print("Sliced tuple from index 2 to 4:", thistuple[2:4])
