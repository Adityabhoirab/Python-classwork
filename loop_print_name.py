# Program: Print your name multiple times using a for loop

# Using the first syntax to print "hello world" 10 times
print("Printing 'hello world' 10 times:")
for i in range(10):
    print("hello world", i)

# Using another syntax to print "aditya" 10 times
print("\nPrinting 'aditya' 10 times:")
for i in range(1, 11):
    print("aditya", i)
